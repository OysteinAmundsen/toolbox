// #region Transform

/**
 * Convert raw MDX source into agent-friendly Markdown.
 *
 * - Strips ESM `import` lines and MDX expression comments.
 * - Inlines `<ShowSource file="…">` and bare demo components as ```ts blocks.
 * - Flattens Starlight `<Tabs>` / `<TabItem label="…">` into `####` headings.
 * - Converts `<LinkCard>` → Markdown link bullets, `<Card title>` → `###`
 *   headings, `<Aside>` → `:::type` directives, `<Badge>` → inline text, and
 *   strips structural wrappers (`<CardGrid>`, `<Steps>`, `<FileTree>`, `<Icon>`).
 * - Leaves remaining directive syntax (`:::tip`) in place — agents tolerate it
 *   and it carries structure; aggressive flattening is deferred.
 */
export function mdxToAgentMarkdown(raw: string, opts: AgentMarkdownOptions): string {
  const { data, body: rawBody } = splitFrontmatter(raw);

  // Resolve `<Audience>` regions first (drop human-only, unwrap agent-only) so the
  // rest of the transform never sees the wrapper tags. Done before the fenced-code
  // split below because a region may straddle a ```code``` fence.
  const audienceResolved = resolveAudienceRegions(rawBody);

  // Optionally scope to a single framework: keep only the matching tab in each
  // framework tab group. Runs before the fenced split for the same straddling
  // reason, while `<Tabs>`/`<TabItem>` structure is still intact.
  const body = opts.frameworkFilter ? filterFrameworkTabs(audienceResolved, opts.frameworkFilter) : audienceResolved;

  // Split into fenced-code vs prose segments. ALL stripping/replacement runs on
  // prose only — example `import` lines inside ```code``` fences are real,
  // copy-ready guidance for the agent and MUST be preserved verbatim.
  const segments = body.split(/(```[\s\S]*?```)/g);
  const isFence = (s: string) => s.startsWith('```');

  // 1. Map imported demo component names → their `.astro` path (prose only).
  //    Only `demos/` imports are inlineable as runnable code. Other `.astro`
  //    imports are interactive doc widgets (e.g. `<ThemeBuilder/>`,
  //    `<CSSVariableReference/>`, `<ShowSource/>`) whose rendered output is
  //    generated at runtime and has NO inlineable source — track their names
  //    separately so step 4b can strip the leftover tags cleanly instead of
  //    emitting a bogus "demo source not found" comment.
  const componentToDemo = new Map<string, string>();
  const nonDemoComponents = new Set<string>();
  const importRe = /^import\s+(\w+)\s+from\s+['"]([^'"]+\.astro)['"];?\s*$/gm;
  for (const seg of segments) {
    if (isFence(seg)) continue;
    for (let m = importRe.exec(seg); m; m = importRe.exec(seg)) {
      const [, name, path] = m;
      if (/(^|\/)demos\//.test(path)) componentToDemo.set(name, path);
      else nonDemoComponents.add(name);
    }
  }

  const transformed = segments.map((seg) => {
    if (isFence(seg)) return seg; // leave code blocks untouched

    let out = seg;
    // 1b. Restore agent-only headings. Inside `<Audience only="agent">` blocks,
    //     section breaks are authored as `_## Heading` (underscore-prefixed) so
    //     Starlight's compile-time table-of-contents harvest — which scans real
    //     ATX headings even inside conditionally-rendered slots — skips them and
    //     the human page gets no phantom TOC links. `_#…` is not a valid heading
    //     in CommonMark, so it never reaches the human site. Here we drop the
    //     leading `_` to turn it back into a true Markdown heading the agent can
    //     navigate by. Runs in prose only, so `_#` inside a code fence is kept.
    out = out.replace(/^_(#{1,6} )/gm, '$1');
    // 2. Drop ESM import lines and MDX comments.
    out = out.replace(/^import\s+.*$/gm, '');
    out = out.replace(/\{\/\*[\s\S]*?\*\/\}/g, '');

    // 2b. Flatten Starlight `<Tabs>` / `<TabItem label="…">` into `####` headings.
    //     Tabs render as interactive widgets in HTML but add noise in plain
    //     Markdown; the tab label is the only meaningful structure, so promote it
    //     to a heading and drop the wrapper tags.
    out = out.replace(/<TabItem\b[^>]*\blabel=["']([^"']+)["'][^>]*>/g, (_full, label: string) => `\n#### ${label}\n`);
    out = out.replace(/<\/TabItem>/g, '');
    out = out.replace(/<Tabs\b[^>]*>/g, '');
    out = out.replace(/<\/Tabs>/g, '');

    // 2c. Flatten remaining Starlight components into plain Markdown. These are
    //     raw JSX in the source MDX; left untouched they leak as literal tags an
    //     agent can't use. All replacements operate on OPEN and CLOSE tags
    //     independently (never as `<X>…</X>` pairs) so they survive the
    //     fenced-code segment split above — a component whose children include a
    //     ```code``` block has its open/close tags in DIFFERENT segments.
    //   - `<LinkCard title description href />` → a Markdown link bullet (the
    //     `href` is rewritten to its `.md` companion by step 5 below).
    out = out.replace(/<LinkCard\b([^>]*?)\/?>/g, (_full, attrs: string) => {
      const title = attrs.match(/\btitle=["']([^"']*)["']/)?.[1] ?? '';
      const href = attrs.match(/\bhref=["']([^"']*)["']/)?.[1] ?? '';
      const description = attrs.match(/\bdescription=["']([^"']*)["']/)?.[1];
      if (!href) return '';
      return `- [${title || href}](${href})${description ? `: ${description}` : ''}`;
    });
    //   - `<Card title="…">` → an `###` heading; bare `<Card>` / `</Card>` drop.
    out = out.replace(/<Card\b([^>]*)>/g, (_full, attrs: string) => {
      const title = attrs.match(/\btitle=["']([^"']*)["']/)?.[1];
      return title ? `\n### ${title}\n` : '';
    });
    out = out.replace(/<\/Card>/g, '');
    //   - `<Aside type="…" [title="…"]>` → the equivalent `:::type[title]`
    //     directive already used throughout the prose; `</Aside>` → closing `:::`.
    out = out.replace(/<Aside\b([^>]*)>/g, (_full, attrs: string) => {
      const type = attrs.match(/\btype=["']([^"']*)["']/)?.[1] ?? 'note';
      const title = attrs.match(/\btitle=["']([^"']*)["']/)?.[1];
      return `:::${type}${title ? `[${title}]` : ''}`;
    });
    out = out.replace(/<\/Aside>/g, ':::');
    //   - `<Badge text="…" />` → its inline text in parens; structural wrappers
    //     (`<CardGrid>`, `<Steps>`, `<FileTree>`) and `<Icon />` carry no textual
    //     value, so strip the tags and keep any inner Markdown.
    out = out.replace(/<Badge\b[^>]*\btext=["']([^"']*)["'][^>]*\/?>/g, (_full, text: string) => `(${text})`);
    out = out.replace(/<Icon\b[^>]*\/?>/g, '');
    out = out.replace(/<\/?(?:CardGrid|Steps|FileTree)\b[^>]*>/g, '');

    // 3. Inline `<ShowSource file="…"> … </ShowSource>` blocks first (the `file`
    //    attribute points directly at the demo, and its children are the same
    //    demo component, so replacing the whole block avoids duplicate code).
    //    When `omitDemoComponents` is set (framework-scoped corpus), emit a
    //    pointer to the page's `.md` companion instead of the vanilla source.
    out = out.replace(
      /<ShowSource\b[^>]*\bfile=["']([^"']+)["'][^>]*>[\s\S]*?<\/ShowSource>/g,
      (_full, file: string) =>
        '\n' +
        (opts.omitDemoComponents ? demoPointer(file, opts.demoPageUrl) : demoCodeBlock(file, opts.resolveDemo)) +
        '\n',
    );

    // 3b. Expand `<AgentSource paths="…" />` directives — an agent-only mechanism
    //     (placed inside `<Audience only="agent">`) that inlines real demo-app
    //     modules from repo-root `demos/` as fenced code, so the corpus carries a
    //     full end-to-end reference implementation from the live demo's own
    //     source. In a framework-scoped corpus, a per-framework attribute
    //     (`react`/`vue`/`angular`) swaps in that framework's native demo set;
    //     otherwise the default `paths` (vanilla) set is used. Self-closing, so
    //     handle before the non-demo-component strip in 4b (which would otherwise
    //     remove the tag and lose the inlined source).
    out = out.replace(/<AgentSource\b[^>]*\/?>/g, (tag: string) => {
      const paths = selectAgentSourcePaths(tag, opts.frameworkFilter);
      return paths ? agentSourceBlock(paths, opts.resolveSource) : '';
    });

    // 4. Inline remaining standalone demo components (`<IntroBasicDemo />` or
    //    `<IntroBasicDemo>…</IntroBasicDemo>`). When `omitDemoComponents` is set
    //    (framework-scoped corpus), emit a `.md` pointer instead of the vanilla
    //    source so the corpus stays free of non-idiomatic `HTMLElement` code.
    for (const [name, demoPath] of componentToDemo) {
      const selfClosing = new RegExp(`<${name}\\b[^>]*/>`, 'g');
      const paired = new RegExp(`<${name}\\b[^>]*>[\\s\\S]*?</${name}>`, 'g');
      const block =
        '\n' +
        (opts.omitDemoComponents
          ? demoPointer(demoPath, opts.demoPageUrl)
          : demoCodeBlock(demoPath, opts.resolveDemo)) +
        '\n';
      out = out.replace(paired, block).replace(selfClosing, block);
    }

    // 4b. Strip leftover tags for imported NON-demo components (interactive doc
    //     widgets like `<ThemeBuilder/>` / `<CSSVariableReference/>` whose output
    //     is generated at runtime, so there is nothing to inline). Without this
    //     they would either leak as raw JSX or — because they are `.astro`
    //     imports — be mis-treated as demos and emit a "demo source not found"
    //     comment. `<ShowSource>` is already consumed in step 3; any stray
    //     instance is dropped here too.
    for (const name of nonDemoComponents) {
      // Special-case: the theming guide's `<CSSVariableReference />` is replaced
      // with an injected Markdown table (variable name + description) rather than
      // stripped, so the CSS-variable reference survives into the agent corpus.
      if (name === 'CSSVariableReference' && opts.cssVarReference) {
        const selfClosing = new RegExp(`<${name}\\b[^>]*/>`, 'g');
        const paired = new RegExp(`<${name}\\b[^>]*>[\\s\\S]*?</${name}>`, 'g');
        out = out.replace(paired, opts.cssVarReference).replace(selfClosing, opts.cssVarReference);
        continue;
      }
      const selfClosing = new RegExp(`<${name}\\b[^>]*/>`, 'g');
      const paired = new RegExp(`<${name}\\b[^>]*>[\\s\\S]*?</${name}>`, 'g');
      out = out.replace(paired, '').replace(selfClosing, '');
    }

    // 5. Rewrite internal doc links from their HTML form to the `.md` companion
    //    so agents keep navigating the markdown graph. Match only the `](url)`
    //    tail (not the link text) so labels containing brackets — e.g.
    //    `[`ColumnConfig[]`](…)` — still rewrite. `hasDoc` gates out images and
    //    any non-doc path, so matching bare `](…)` is safe.
    out = out.replace(/\]\((\/[^)\s]+)\)/g, (full, url: string) => {
      const rewritten = rewriteDocLink(url, opts.hasDoc);
      return rewritten === url ? full : `](${rewritten})`;
    });
    return out;
  });

  // 6. Rejoin and collapse the blank lines left behind by stripped content.
  //    Normalise whitespace-only lines first (stripped wrapper tags were often
  //    indented, leaving lines of stray spaces) so the blank-line collapse works.
  let out = transformed
    .join('')
    .replace(/^[ \t]+$/gm, '')
    .replace(/\n{3,}/g, '\n\n')
    .trim();

  // 6b. Drop illustrative/navigational H2 sections requested by the corpus
  //     builder (e.g. `## Demos`, `## See Also`). Runs after the body is fully
  //     transformed so it sees real `##` headings, and collapses the blank lines
  //     the removal leaves behind. Skipped entirely when no sections are listed.
  if (opts.dropSections?.length) {
    out = dropMarkdownSections(out, opts.dropSections)
      .replace(/\n{3,}/g, '\n\n')
      .trim();
  }

  // 6c. Inverse filter: keep only the preamble + listed H2 sections (e.g. demote
  //     plugin pages to intro + Installation + Basic Usage). Runs after 6b so it
  //     sees real `##` headings; collapses the blank lines the removal leaves.
  if (opts.keepSections?.length) {
    out = keepMarkdownSections(out, opts.keepSections)
      .replace(/\n{3,}/g, '\n\n')
      .trim();
  }

  // 7. Prepend a title heading + description so the page is self-describing.
  const header: string[] = [];
  if (data.title) header.push(`# ${data.title}`);
  if (data.description) header.push(`> ${data.description}`);
  return (header.length ? header.join('\n\n') + '\n\n' : '') + out + '\n';
}

