/**
 * _llm-markdown.ts — Pure MDX → agent-markdown transform (no Astro/Vite deps).
 *
 * Underscore prefix keeps this OUT of the route table; it is a helper imported
 * by the `[...slug].md.ts` endpoint. Kept pure (demo resolution is injected) so
 * it can be unit-tested without a running Vite/Astro pipeline.
 *
 * Goal: turn a human-facing `.mdx` doc page into a lean, self-contained Markdown
 * document that an AI agent can fetch in isolation. The critical job is
 * **inlining demo `<script>` blocks** — in the source MDX the runnable code lives
 * inside imported `.astro` demo components (`<IntroBasicDemo />`), so an agent that
 * only sees the MDX would get prose plus an opaque component tag. We resolve each
 * demo back to its `.astro` source and inline the script as a fenced code block.
 */

// #region Types

/**
 * Target framework for a framework-scoped agent corpus
 * (`llms-full-{framework}.txt`). `'vanilla'` keeps the plain TypeScript/JS/HTML
 * examples and drops the React/Vue/Angular adapter variants.
 */
export type Framework = 'vanilla' | 'react' | 'vue' | 'angular';

export interface AgentMarkdownOptions {
  /**
   * Resolve a demo `.astro` path (e.g. `demos/IntroBasicDemo.astro`) to its raw
   * source text, or `undefined` if it cannot be found.
   */
  resolveDemo: (relPath: string) => string | undefined;
  /**
   * Resolve a repo-relative demo-app source path (e.g.
   * `demos/vanilla/src/demos/employee-management/grid-config.ts`) to its raw
   * text, or `undefined` if not found. Drives the `<AgentSource paths="…" />`
   * directive, which inlines a full end-to-end reference implementation into the
   * agent corpus from the live demo's own source. When omitted, `<AgentSource>`
   * tags are stripped.
   */
  resolveSource?: (relPath: string) => string | undefined;
  /**
   * Return `true` if the given lowercase doc slug (e.g. `grid/getting-started`)
   * has a generated `.md` companion page. Used to rewrite internal HTML links to
   * their `.md` form so agents stay in the markdown graph. When omitted, internal
   * links are left untouched.
   */
  hasDoc?: (slug: string) => boolean;
  /**
   * Markdown to substitute for a `<CSSVariableReference />` widget tag (the theming
   * guide's live CSS-variable table). Generated by the endpoint from the shared
   * `CSS_VARIABLES` registry — see `cssVariableReferenceMarkdown` in `_llm-sources`.
   * Injected here so the transform need not import doc data and stay pure. When
   * omitted, the tag is stripped like any other non-demo widget.
   */
  cssVarReference?: string;
  /**
   * When set, produce a framework-scoped corpus: within framework tab groups
   * (those offering React/Vue/Angular alternatives), keep only the variant for
   * this framework and drop the rest. Neutral tab groups (e.g. npm/yarn/CDN) and
   * pages with no framework tabs are left untouched. When omitted, every tab
   * variant is kept.
   */
  frameworkFilter?: Framework;
  /**
   * Lowercase H2 heading texts whose whole section (the `## Heading` line through
   * everything up to the next `##`/`#`) is removed from the output. Used by the
   * `llms-full.txt` corpus builder to drop illustrative/navigational sections —
   * `## Demos`, `## Demo`, `## See Also` — that inline large demo `.astro` dumps
   * or pure link lists and carry no API truth, while keeping every API-bearing
   * section (Installation, Basic Usage, Configuration Options, Programmatic API,
   * Events, Keyboard Shortcuts). NOT passed by the per-page `.md` endpoint, so a
   * single-page fetch still includes its demos. Matched case-insensitively after
   * stripping any `[text](url)` link wrapper from the heading.
   */
  dropSections?: string[];
  /**
   * Inverse of `dropSections`: when set, the output keeps ONLY the page preamble
   * (everything before the first `##`) plus the listed H2 sections; every other
   * H2 section is removed. Used by the `llms-full.txt` corpus builder to demote
   * plugin pages to a compact "how to enable + basic usage" stub (intro +
   * `## Installation` + `## Basic Usage`), with the deep API (configuration
   * tables, events, methods) linked as a per-page `.md` companion rather than
   * inlined — the dominant corpus-size driver. NOT passed by the per-page `.md`
   * endpoint, so a single-page fetch still includes the full plugin API. Matched
   * case-insensitively after stripping any `[text](url)` link wrapper. Applied
   * after `dropSections`.
   */
  keepSections?: string[];
  /**
   * When `true`, bare demo components (`<DemoName />`) and `<ShowSource file>`
   * blocks are NOT inlined as code; each is replaced by a one-line pointer to the
   * page's `.md` companion (`demoPageUrl`). Used by the per-framework corpus
   * variants (`llms-full-{react,vue,angular}.txt`): the demo `.astro` sources are
   * vanilla-only (`queryGrid` + `HTMLElement` renderers), so inlining them into a
   * framework-scoped corpus contradicts that framework's "use components, not
   * HTMLElements" guidance. The framework-native snippet in the adjacent tab group
   * is the idiomatic example; the full vanilla reference stays one fetch away.
   * `<AgentSource>` (which has framework-native `react`/`vue`/`angular` overrides)
   * is unaffected. NOT passed by the per-page `.md` endpoint, so single-page
   * fetches still inline every demo.
   */
  omitDemoComponents?: boolean;
  /**
   * Absolute `.md` URL of the page being transformed, used as the link target in
   * the pointer emitted when `omitDemoComponents` is set. When omitted, the
   * pointer falls back to a generic "see the page's `.md` companion" note.
   */
  demoPageUrl?: string;
}

// #endregion

// #region Frontmatter

interface Frontmatter {
  title?: string;
  description?: string;
  /**
   * When explicitly `false`, the page is omitted from the inlined `llms-full.txt`
   * corpus (it stays linked in the `llms.txt` index). Used to keep sales/marketing
   * pages — e.g. release-history changelogs — out of the one-shot ingestion file
   * while leaving them discoverable. Defaults to included.
   */
  llmsFull?: boolean;
}

/** Split leading `---` frontmatter from the body. Minimal, no YAML dependency. */
function splitFrontmatter(raw: string): { data: Frontmatter; body: string } {
  const match = raw.match(/^---\r?\n([\s\S]*?)\r?\n---\r?\n?/);
  if (!match) return { data: {}, body: raw };
  const data: Frontmatter = {};
  for (const line of match[1].split(/\r?\n/)) {
    const kv = line.match(/^(\w+):\s*(.*)$/);
    if (!kv) continue;
    const key = kv[1];
    let value = kv[2].trim();
    // Strip surrounding quotes if present.
    if ((value.startsWith('"') && value.endsWith('"')) || (value.startsWith("'") && value.endsWith("'"))) {
      value = value.slice(1, -1);
    }
    if (key === 'title' || key === 'description') data[key] = value;
    else if (key === 'llmsFull') data.llmsFull = value !== 'false';
  }
  return { data, body: raw.slice(match[0].length) };
}

/**
 * Extract the `title` / `description` / `llmsFull` frontmatter from a raw MDX
 * page. Used by `llms.txt.ts` to label each index link, and by `llms-full.txt.ts`
 * to decide whether to inline a page, without running the full body transform.
 */
export function extractFrontmatter(raw: string): { title?: string; description?: string; llmsFull?: boolean } {
  return splitFrontmatter(raw).data;
}

// #endregion

// #region Audience regions

/**
 * Resolve `<Audience only="human|agent">…</Audience>` regions for the agent
 * corpus. Runs on the WHOLE body BEFORE the fenced-code split so a region whose
 * children include a ```code``` fence is handled as one unit (the open/close
 * tags would otherwise land in different fence segments).
 *
 * - `only="human"` regions (sales/marketing copy, motivational asides) are
 *   removed entirely — they render on the HTML site but must not pollute the
 *   implementation guidance an agent ingests.
 * - `only="agent"` regions (steering directives, extra gotchas, recipes) have
 *   only their wrapper tags removed; the inner Markdown is KEPT and flows through
 *   the rest of the transform. They render as nothing on the HTML site (see
 *   `Audience.astro`).
 *
 * Nesting is not supported (the non-greedy match stops at the first
 * `</Audience>`); do not nest `<Audience>` blocks.
 */
export function resolveAudienceRegions(body: string): string {
  return body
    .replace(/<Audience\b[^>]*\bonly=["']human["'][^>]*>[\s\S]*?<\/Audience\s*>/g, '')
    .replace(/<Audience\b[^>]*\bonly=["']agent["'][^>]*>([\s\S]*?)<\/Audience\s*>/g, '$1');
}

// #endregion

// #region Demo inlining

/** Extract the first `<script>` block's inner content from a demo `.astro` file. */
function extractDemoScript(astroSource: string): string | undefined {
  // Allow whitespace/attributes before the closing `>` (e.g. `</script >`) so the
  // pattern matches the same forgiving end tags a browser parser accepts
  // (CodeQL js/bad-tag-filter).
  const match = astroSource.match(/<script[^>]*>([\s\S]*?)<\/script\s*>/i);
  if (!match) return undefined;
  return match[1].replace(/^\r?\n/, '').replace(/\s+$/, '');
}

/** Build a fenced code block from a demo's script, or a comment if unavailable. */
function demoCodeBlock(relPath: string, resolveDemo: (p: string) => string | undefined): string {
  const source = resolveDemo(relPath);
  if (!source) return `<!-- demo source not found: ${relPath} -->`;
  const script = extractDemoScript(source);
  if (!script) return `<!-- demo has no <script> block: ${relPath} -->`;
  // Prefix the block with the demo's filename so an agent reading the inlined
  // code knows which `.astro` demo source it represents (provenance hint).
  const fileName = relPath.split(/[\\/]/).pop() ?? relPath;
  return '```ts\n// ' + fileName + '\n' + script + '\n```';
}

/**
 * One-line pointer that REPLACES an inlined demo when `omitDemoComponents` is set
 * (the per-framework corpus variants). The demo `.astro` sources are vanilla-only,
 * so a framework-scoped corpus links to the full reference instead of inlining
 * non-idiomatic code. Links to the page's `.md` companion when known.
 */
function demoPointer(relPath: string, pageUrl?: string): string {
  const fileName = relPath.split(/[\\/]/).pop() ?? relPath;
  return pageUrl
    ? `> Runnable demo \`${fileName}\` (vanilla reference) omitted from this framework-scoped corpus — see the [full example](${pageUrl}); the framework-native snippet above is the idiomatic version.`
    : `> Runnable demo \`${fileName}\` (vanilla reference) omitted — see the page's \`.md\` companion for the full example.`;
}

/**
 * Expand an `<AgentSource paths="a, b, c" />` directive into one fenced code
 * block per referenced demo-app module, each prefixed with a `// <repo path>`
 * provenance comment. Paths are comma-separated and repo-relative; the language
 * is inferred from the extension (`.css` → css, `.vue` → vue, `.tsx` → tsx,
 * `.html` → html, else ts). A path that cannot be resolved emits an HTML comment
 * instead of failing the build. Returns an empty string when no resolver is
 * supplied (tags are simply dropped).
 */
function agentSourceBlock(pathsAttr: string, resolveSource?: (p: string) => string | undefined): string {
  if (!resolveSource) return '';
  const blocks = pathsAttr
    .split(',')
    .map((p) => p.trim())
    .filter(Boolean)
    .map((p) => {
      const source = resolveSource(p);
      if (!source) return `<!-- agent source not found: ${p} -->`;
      const lang = langOfPath(p);
      return '```' + lang + '\n// ' + p + '\n' + source.replace(/\s+$/, '') + '\n```';
    });
  return '\n' + blocks.join('\n\n') + '\n';
}

/** Infer the fenced-code language tag for a demo-source path. */
function langOfPath(p: string): string {
  if (/\.css$/.test(p)) return 'css';
  if (/\.vue$/.test(p)) return 'vue';
  if (/\.tsx$/.test(p)) return 'tsx';
  if (/\.html$/.test(p)) return 'html';
  return 'ts';
}

/**
 * Pick the path list for an `<AgentSource>` tag, honouring per-framework
 * overrides. The default `paths` attribute is the vanilla / cross-framework set;
 * optional `react`/`vue`/`angular` attributes supply a framework-native set that
 * REPLACES it when the corpus is scoped to that framework. `vanilla` has no
 * dedicated attribute — `paths` already is the vanilla list. Returns `undefined`
 * when the tag has no usable path list.
 */
function selectAgentSourcePaths(tag: string, framework?: Framework): string | undefined {
  const attr = (name: string): string | undefined => {
    const m = tag.match(new RegExp(`\\b${name}=["']([^"']+)["']`));
    return m ? m[1] : undefined;
  };
  const override = framework && framework !== 'vanilla' ? attr(framework) : undefined;
  return override ?? attr('paths');
}

